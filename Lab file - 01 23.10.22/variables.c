#include <stdio.h>
int main()
{
    int _int = 4;
    char _char = 'K';
    float _float = 3.1416;
    char _string[] = {"Kausar Ahmed"};

    printf("\n\nThis is Integer type data: %d", _int);
    printf("\n\nThis is Character type data: %c", _char);
    printf("\n\nThis is float type data: %f", _float);
    printf("\n\nThis is String type data: %s", _string);
}